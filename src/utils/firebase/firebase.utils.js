const googleProvider = new GoogleAuthProvider();

googleProvider.setCustomParameters({
  prompt: 'select_account',
});

export const auth = getAuth();
export const signInWithGooglePopup = () => signInWithPopup(auth, googleProvider);
export const signInWithGoogleRedirect = () => signInWithRedirect(auth, googleProvider)

export const db = getFirestore();

export const addCollectionAndDocuments = async (
  collectionKey,
  objectsToAdd
) => {
  const batch = writeBatch(db);
  const collectionRef = collection(db, collectionKey);
  
  objectsToAdd.forEach((object) => {
     const docRef = doc(collectionRef, object.title.toLowerCase());
     batch.set(docRef, object);
  });

  await batch.commit();
  console.log('done');
};

export const getCategoriesAndDocuments = async () => {
  const collectionRef = collection(db, 'categories');
  const q = query(collectionRef);

  const querySnapshot = await getDocs(q);
  const categoryMap = querySnapshot.docs.reduce((acc, docSnapshot) => {
    const { title, items } = docSnapshot.data();
    acc[title.toLowerCase()] = items;
    return acc;
  }, {});

  return categoryMap;
};

export const createUserDocumentFromAuth = async (userAuth, additionalInformation = {}) => {
  if (!userAuth) return;

  // Döküman bilgilerini oluşturur
  const userDocRef = doc(db, 'users', userAuth.uid);

  // Döküman içindeki bilgileri alır.
  const userSnapshot = await getDoc(userDocRef);

  // Dökümanda  böyle bir verinin olup olmadığını kontrol eder.
  if (!userSnapshot.exists()) {
    // Google'ın geri döndürdüğü user bilgilerini kullanabilmek için yayar.
    const { displayName, email } = userAuth;
    const createdAt = new Date();
    // Bilgileri dökümana kayıt eder.
    try {
      await setDoc(userDocRef, {
        displayName,
        email,
        createdAt,
        // additionalInformation kullanıcı hakkında almak istediğimiz diğer bilgileri temsil eder.
        ...additionalInformation
      });
    } catch (error) {
      console.log('error creating the user', error.message);
    }
  }

  return userDocRef;
};

export const createAuthUserWithEmailAndPassword = async (email, password) => {
  if (!email || !password) return
  return await createUserWithEmailAndPassword(auth, email, password)
}

export const signInAuthUserWithEmailAndPassword = async (email, password) => {
  if (!email || !password) return
  return await signInWithEmailAndPassword(auth, email, password)
}

export const signOutUser = async () => await signOut(auth)

// Auth singleton'u dinler.
export const onAuthStateChangedListener = (callback) => onAuthStateChanged(auth, callback)